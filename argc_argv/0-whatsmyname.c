#include <stdio.h>

/**
 * main - Entry point, prints the name of the program
 * @argc: The number of arguments passed to the program
 * @argv: Array of pointers to the arguments
 * Return: Always 0
 */

int main(int __attribute__((unused)) argc, char *argv[])
{
printf("%s\n", argv[0]);
return (0);
}
